#include <cstdio>
#include <cstdlib>
#include <vector>
#include <algorithm>
#include <time.h>

static inline double diff_sec(struct timespec a, struct timespec b) {
    return (b.tv_sec - a.tv_sec) + (b.tv_nsec - a.tv_nsec) / 1e9;
}

std::vector<int> load_array(const char* filename, size_t& n) {
    std::vector<int> data;
    FILE* fp = fopen(filename, "rb");
    if (!fp) {
        fprintf(stderr, "Error: Cannot open %s for reading\n", filename);
        return data;
    }

    if (fread(&n, sizeof(size_t), 1, fp) != 1) {
        fprintf(stderr, "Error: Cannot read size from %s\n", filename);
        fclose(fp);
        return data;
    }

    data.resize(n);
    if (fread(data.data(), sizeof(int), n, fp) != n) {
        fprintf(stderr, "Error: Cannot read data from %s\n", filename);
        data.clear();
        fclose(fp);
        return data;
    }

    fclose(fp);
    printf("Loaded %zu elements from %s\n", n, filename);
    return data;
}

static void merge_cpu(std::vector<int>& a, std::vector<int>& buf,
                      int left, int mid, int right) {
    int i = left, j = mid + 1, k = left;

    while (i <= mid && j <= right) {
        if (a[i] <= a[j]) buf[k++] = a[i++];
        else              buf[k++] = a[j++];
    }
    while (i <= mid)   buf[k++] = a[i++];
    while (j <= right) buf[k++] = a[j++];

    for (int t = left; t <= right; ++t) a[t] = buf[t];
}

static void mergesort_rec(std::vector<int>& a, std::vector<int>& buf,
                          int left, int right) {
    if (left >= right) return;
    int mid = left + ((right - left) >> 1);

    mergesort_rec(a, buf, left, mid);
    mergesort_rec(a, buf, mid + 1, right);
    merge_cpu(a, buf, left, mid, right);
}

void mergesort(std::vector<int>& a) {
    if (a.empty()) return;
    std::vector<int> buf(a.size());
    mergesort_rec(a, buf, 0, (int)a.size() - 1);
}

int main(int argc, char** argv) {
    if (argc < 2) {
        printf("Usage: %s <data_file>\n", argv[0]);
        printf("  data_file: Binary file generated by gen_vector\n");
        return 1;
    }

    const char* data_file = argv[1];

    size_t N;
    std::vector<int> a = load_array(data_file, N);
    if (a.empty()) return 1;

    struct timespec t0, t1;
    clock_gettime(CLOCK_MONOTONIC, &t0);
    mergesort(a);
    clock_gettime(CLOCK_MONOTONIC, &t1);

    double elapsed_sec = diff_sec(t0, t1);

    bool ok = std::is_sorted(a.begin(), a.end());
    printf("Mergesort(%zu) = %s\n", N, ok ? "Correct" : "Incorrect");
    printf("Execution time: %.3f ms\n", elapsed_sec * 1000.0);

    return ok ? 0 : 1;
}

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <omp.h>
#include <time.h>
#include <limits.h>

#ifndef TASK_SPAWN_CUTOFF
#define TASK_SPAWN_CUTOFF (4096)
#endif

// Global data array and buffer
static int* g_data = NULL;
static int* g_buf = NULL;

// Merge [left..mid] and [mid+1..right] from g_data into g_buf, then copy back
static inline void merge(int* a, int* buf, int left, int mid, int right) {
    int i = left, j = mid + 1, k = left;
    while (i <= mid && j <= right) {
        if (a[i] <= a[j])  buf[k++] = a[i++];
        else               buf[k++] = a[j++];
    }
    while (i <= mid)   buf[k++] = a[i++];
    while (j <= right) buf[k++] = a[j++];
    for (int t = left; t <= right; ++t) a[t] = buf[t];
}

// Recursive merge sort (used for small ranges)
static void mergesort_rec(int* a, int left, int right) {
    if (left >= right) return;
    int mid = left + ((right - left) >> 1);
    mergesort_rec(a, left, mid);
    mergesort_rec(a, mid + 1, right);
    merge(a, g_buf, left, mid, right);
}

// Parallel merge sort task
static void mergesort_task(int left, int right) {
    int n = right - left + 1;
    
    // Base case: single element or empty range
    if (left >= right) return;
    
    if (n > TASK_SPAWN_CUTOFF) {
        int mid = left + ((right - left) >> 1);
        #pragma omp task
        mergesort_task(left, mid);
        #pragma omp task
        mergesort_task(mid + 1, right);
        #pragma omp taskwait
        merge(g_data, g_buf, left, mid, right);
    } else {
        mergesort_rec(g_data, left, right);
    }
}

// Compare function for qsort
static int compare_ints(const void* a, const void* b) {
    int ia = *(const int*)a;
    int ib = *(const int*)b;
    return (ia > ib) - (ia < ib);
}

// Load array from binary file
static int* load_array(const char* filename, size_t* n) {
    FILE* fp = fopen(filename, "rb");
    if (!fp) {
        fprintf(stderr, "Error: Cannot open %s for reading\n", filename);
        return NULL;
    }
    
    // Read size
    if (fread(n, sizeof(size_t), 1, fp) != 1) {
        fprintf(stderr, "Error: Cannot read size from %s\n", filename);
        fclose(fp);
        return NULL;
    }
    
    // Allocate and read data
    int* data = (int*)malloc(sizeof(int) * (*n));
    if (!data) {
        fprintf(stderr, "Error: Cannot allocate memory for %zu elements\n", *n);
        fclose(fp);
        return NULL;
    }
    
    if (fread(data, sizeof(int), *n, fp) != *n) {
        fprintf(stderr, "Error: Cannot read data from %s\n", filename);
        free(data);
        fclose(fp);
        return NULL;
    }
    
    fclose(fp);
    printf("Loaded %zu elements from %s\n", *n, filename);
    return data;
}

// Verify that array is sorted
static int is_sorted(int* arr, int n) {
    for (int i = 1; i < n; i++) {
        if (arr[i-1] > arr[i]) {
            return 0;
        }
    }
    return 1;
}

int main(int argc, char** argv) {
    if (argc < 2) {
        printf("Usage: %s <data_file> [reference_file]\n", argv[0]);
        printf("  data_file: Binary file containing array data (generated by gen_vector)\n");
        printf("  reference_file: Optional reference file for verification\n");
        return 1;
    }
    
    const char* data_file = argv[1];
    const char* reference_file = (argc > 2) ? argv[2] : NULL;
    
    // Load data from file
    size_t N;
    int* data = load_array(data_file, &N);
    if (!data) {
        return 1;
    }
    
    // Create reference for verification
    int* reference = NULL;
    if (reference_file) {
        // Load reference from file
        size_t ref_n;
        reference = load_array(reference_file, &ref_n);
        if (!reference || ref_n != N) {
            fprintf(stderr, "Error: Reference file size mismatch\n");
            free(data);
            if (reference) free(reference);
            return 1;
        }
    } else {
        // Create reference by sorting data
        reference = (int*)malloc(sizeof(int) * N);
        if (!reference) {
            fprintf(stderr, "Failed to allocate reference array\n");
            free(data);
            return 1;
        }
        memcpy(reference, data, sizeof(int) * N);
        qsort(reference, N, sizeof(int), compare_ints);
    }
    
    g_data = data;
    g_buf = (int*)malloc(sizeof(int) * N);
    if (!g_buf) {
        fprintf(stderr, "Failed to allocate buffer\n");
        return 1;
    }
    
    #pragma omp parallel
    {
        #pragma omp single
        {
            /* no op */
        }
    }

    // Time the merge sort
    double start = omp_get_wtime();
    #pragma omp parallel
    {
        #pragma omp single
        mergesort_task(0, N - 1);
    }
    double end = omp_get_wtime();
    free(g_buf);

    // Verify correctness
    int correct = is_sorted(data, N);
    if (correct) {
        for (size_t i = 0; i < N; i++) {
            if (data[i] != reference[i]) {
                correct = 0;
                break;
            }
        }
    }
    
    printf("Mergesort(%zu) = %s\n", N, correct ? "Correct" : "Incorrect");
    printf("Execution time: %.3f ms\n", (end - start) * 1000);
    
    free(data);
    free(reference);
    return correct ? 0 : 1;
}

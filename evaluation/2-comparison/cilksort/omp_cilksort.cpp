#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <omp.h>

#define TASK_SPAWN_CUTOFF_SORT (4096)
#define TASK_SPAWN_CUTOFF_MERGE (4096)

#define SWAP(type, x, y)                                                       \
  {                                                                            \
    type t = x;                                                                \
    x = y;                                                                     \
    y = t;                                                                     \
  }

// Type definitions
typedef int elem_t;     /* Element type */
typedef elem_t *elem_p; /* Element pointer type */
typedef int idx_t;      /* Index type */

elem_t compare_elem_t(const void *a, const void *b) {
  int ia = *(const int*)a;
  int ib = *(const int*)b;
  return (ia > ib) - (ia < ib);
}

static int is_sorted(int* arr, int n) {
  for (int i = 1; i < n; i++) {
      if (arr[i-1] > arr[i]) {
          return 0;
      }
  }
  return 1;
}

// Sequential merge
void merge(elem_p a, idx_t a_len, elem_p b, idx_t b_len, elem_p dst) {
  idx_t i = 0; // index for a
  idx_t j = 0; // index for b
  idx_t ti = 0;

  while (i < a_len && j < b_len) {
    if (a[i] < b[j])
      dst[ti++] = a[i++];
    else
      dst[ti++] = b[j++];
  }

  while (i < a_len)
    dst[ti++] = a[i++];
  while (j < b_len)
    dst[ti++] = b[j++];
}

// return the largest index s.t. x[index] <= val
idx_t binary_search(elem_p x, idx_t len, elem_t val) {
  idx_t low = 0;
  idx_t high = len;
  while (low + 1 < high) {
    idx_t mid = (low + high) / 2;
    if (x[mid] <= val)
      low = mid;
    else
      high = mid;
  }

  return low;
}

// Parallel merge function
void cilkmerge(elem_p a, idx_t a_len, elem_p b, idx_t b_len, elem_p dst) {
  if (a_len < b_len) {
    // Make sure a_len >= b_len
    SWAP(elem_p, a, b);
    SWAP(idx_t, a_len, b_len);
  }

  if (b_len == 0) {
    memcpy(dst, a, a_len * sizeof(elem_t));
    return;
  }

  // Single-threaded merge for small arrays
  if (a_len + b_len < TASK_SPAWN_CUTOFF_MERGE) {
    merge(a, a_len, b, b_len, dst);
    return;
  }

  idx_t a_split = (a_len + 1) / 2;
  idx_t b_split = binary_search(b, b_len, a[a_split - 1]) + 1;

#pragma omp task shared(a, a_split, b, b_split, dst)
  cilkmerge(a, a_split, b, b_split, dst);

#pragma omp task shared(a, a_split, b, b_split, dst)
  {
    elem_p a2 = a + a_split;
    elem_p b2 = b + b_split;
    idx_t a2_len = a_len - a_split;
    idx_t b2_len = b_len - b_split;
    elem_p dst2 = dst + a_split + b_split;
    cilkmerge(a2, a2_len, b2, b2_len, dst2);
  }

#pragma omp taskwait
}

void cilksort(elem_p arr, idx_t n, elem_p tmp) {
  if (n < 2)
    return;

  // Single-threaded sort for small arrays
  if (n < TASK_SPAWN_CUTOFF_SORT) {
    qsort(arr, n, sizeof(elem_t), compare_elem_t);
    return;
  }

  idx_t len12 = n / 2;
  idx_t len1 = len12 / 2;
  idx_t len2 = len12 - len1;
  elem_p a1 = arr, b1 = tmp;
  elem_p a2 = a1 + len1, b2 = b1 + len1;

  idx_t len34 = n - len12;
  idx_t len3 = len34 / 2;
  idx_t len4 = len34 - len3;
  elem_p a3 = arr + len12, b3 = tmp + len12;
  elem_p a4 = a3 + len3, b4 = b3 + len3;

#pragma omp task shared(a1, len1, b1)
  cilksort(a1, len1, b1);
#pragma omp task shared(a2, len2, b2)
  cilksort(a2, len2, b2);
#pragma omp task shared(a3, len3, b3)
  cilksort(a3, len3, b3);
#pragma omp task shared(a4, len4, b4)
  cilksort(a4, len4, b4);
#pragma omp taskwait

#pragma omp task shared(a1, len1, a2, len2, b1)
  cilkmerge(a1, len1, a2, len2, b1);
#pragma omp task shared(a3, len3, a4, len4, b3)
  cilkmerge(a3, len3, a4, len4, b3);
#pragma omp taskwait

  cilkmerge(b1, len12, b3, len34, arr);
}

// Load array from binary file
static elem_p load_array(const char* filename, size_t* n) {
  FILE* fp = fopen(filename, "rb");
  if (!fp) {
    fprintf(stderr, "Error: Cannot open %s for reading\n", filename);
    return NULL;
  }
  
  // Read size
  if (fread(n, sizeof(size_t), 1, fp) != 1) {
    fprintf(stderr, "Error: Cannot read size from %s\n", filename);
    fclose(fp);
    return NULL;
  }
  
  // Allocate and read data
  elem_p data = (elem_p)malloc(sizeof(elem_t) * (*n));
  if (!data) {
    fprintf(stderr, "Error: Cannot allocate memory for %zu elements\n", *n);
    fclose(fp);
    return NULL;
  }
  
  if (fread(data, sizeof(elem_t), *n, fp) != *n) {
    fprintf(stderr, "Error: Cannot read data from %s\n", filename);
    free(data);
    fclose(fp);
    return NULL;
  }
  
  fclose(fp);
  printf("Loaded %zu elements from %s\n", *n, filename);
  return data;
}

int main(int argc, char *argv[]) {
  if (argc < 2) {
    printf("Usage: %s <data_file> [reference_file]\n", argv[0]);
    printf("  data_file: Binary file containing array data (generated by gen_vector)\n");
    printf("  reference_file: Optional reference file for verification\n");
    return 1;
  }
  
  const char* data_file = argv[1];
  const char* reference_file = (argc > 2) ? argv[2] : NULL;
  
  // Load data from file
  size_t N;
  elem_p data = load_array(data_file, &N);
  if (!data) {
    return 1;
  }
  
  // Create reference for verification
  elem_p reference = NULL;
  if (reference_file) {
    // Load reference from file
    size_t ref_n;
    reference = load_array(reference_file, &ref_n);
    if (!reference || ref_n != N) {
      fprintf(stderr, "Error: Reference file size mismatch\n");
      free(data);
      if (reference) free(reference);
      return 1;
    }
  } else {
    // Create reference by sorting data
    reference = (elem_p)malloc(sizeof(elem_t) * N);
    if (!reference) {
      fprintf(stderr, "Failed to allocate reference array\n");
      free(data);
      return 1;
    }
    memcpy(reference, data, sizeof(elem_t) * N);
    qsort(reference, N, sizeof(elem_t), compare_elem_t);
  }
  
  elem_p tmp = (elem_p)malloc(sizeof(elem_t) * N);
  if (!tmp) {
    fprintf(stderr, "Failed to allocate temporary buffer\n");
    free(data);
    free(reference);
    return 1;
  }
  
  #pragma omp parallel
  {
    #pragma omp single
    {
      /* no op */
    }
  }

  double start_time = omp_get_wtime();
  #pragma omp parallel
  {
    #pragma omp single
    {
      cilksort(data, N, tmp);
    }
  }
  double end_time = omp_get_wtime();
  
  int correct = is_sorted(data, N);  
  printf("Cilksort(%zu) = %s\n", N, correct ? "Correct" : "Incorrect");
  printf("Execution time: %.3f ms\n", (end_time - start_time) * 1000);
  
  free(data);
  free(tmp);
  free(reference);
  return correct ? 0 : 1;
}
